[RequireComponent(typeof(MeshFilter))]
public class Voxels : MonoBehaviour {
    
    public Vector3Int size = new Vector3Int(8, 8, 8);
    public float surface = 0;
    public bool surfaceInterpolation = true;
    public bool smoothShading = true;

    private Voxel[,,] voxels = new Voxel[8, 8, 8];

    private Vector3?[,,,] vertexMap = new Vector3?[8, 8, 8, 3];
    private int[,,,] indexMap = new int[8, 8, 8, 3];
    private List<Vector3> vertices = new List<Vector3>();
    private List<int> triangles = new List<int>();
    private MeshFilter meshFilter;

    [Header("Debug")]
    public bool drawBounds;
    public bool drawVoxels;
    public bool drawVerticies;

    [ContextMenu("Position Verticies")]
    private void PositionVerticies() {
        vertexMap = new Vector3?[size.x, size.y, size.z, 3];
        indexMap = new int[size.x, size.y, size.z, 3];
        
        for (int x = 0; x < size.x; x++) {
            for (int y = 0; y < size.y; y++) {
                for (int z = 0; z < size.z; z++) {
                        PositionVertex(x, y, z, 0);
                        PositionVertex(x, y, z, 1);
                        PositionVertex(x, y, z, 2);
                }
            }
        }

    }

    private void PositionVertex(int x, int y, int z, int i) {
        Vector3Int position = new Vector3Int(x, y, z);
        Vector3Int offset = position + GetCorner(4 >> i);
        float A = voxels[x, y, z].value;
        float B;

        if (offset.x == size.x || offset.y == size.y || offset.z == size.z) {
            B = 0;
        } else {
            B = voxels[offset.x, offset.y, offset.z].value;
        }

        float val = Mathf.InverseLerp(A, B, surface);

        if (val == 0 || val == 1) {
            vertexMap[x, y, z, i] = null;
        } else {
            if (surfaceInterpolation){  
                vertexMap[x, y, z, i] = Vector3.Lerp(position, offset, val);
            } else {
                vertexMap[x, y, z, i] = (Vector3)(position + offset) / 2;
            }
        }
    }

    [ContextMenu("Create Mesh Data")]
    void CreateMeshData()
    {
        ClearMeshData();
        PositionVerticies();
        for (int x = 0; x < size.x - 1; x++) {
            for (int y = 0; y < size.y - 1; y++) {
                for (int z = 0; z < size.z - 1; z++) {
                    float[] cube = new float[8];
                    for (int i = 0; i < 8; i++) {
                        Vector3Int corner = new Vector3Int(x, y, z) + GetCorner(CornerTable[i]);
                        cube[i] = voxels[corner.x, corner.y, corner.z].value;
                    }
                    MarchCube(new Vector3Int(x, y, z), cube);
                }
            }
        }
        BuildMesh();
    }

    private void MarchCube (Vector3Int position, float[] cube) {
        int configIndex = GetCubeConfiguration(cube);

        if (configIndex == 0 || configIndex == 255) {
            return;
        }

        int edgeIndex = 0;
        for (int i = 0; i < 5; i++) {
            for (int p = 0; p < 3; p++) {
                int indice = TriangleTable[configIndex, edgeIndex];

                if (indice == -1) {
                    return;
                }

                Vector3Int vert = position + GetCorner(EdgeTable[indice, 0]);
                int vertIndex = EdgeTable[indice, 1];

                if (vertexMap[vert.x, vert.y, vert.z, vertIndex] == null) {
                    Debug.Log("Null Vertex");
                    return;
                }

                Vector3 vertex = (Vector3)vertexMap[vert.x, vert.y, vert.z, vertIndex] + new Vector3(0.5f, 0.5f, 0.5f);           
                

                if (smoothShading) {
                    int index = indexMap[vert.x, vert.y, vert.z, vertIndex];
                    if (index == 0) {
                        vertices.Add(vertex);
                        triangles.Add(vertices.Count - 1);
                        indexMap[vert.x, vert.y, vert.z, vertIndex] = vertices.Count - 1;
                    } else {
                        triangles.Add(index);
                    }
                } else {
                    vertices.Add(vertex);
                    triangles.Add(vertices.Count - 1);
                }
                edgeIndex++;
            }
        }
    }

    int GetCubeConfiguration(float[] cube)
    {
        int configurationIndex = 0;
        for (int i = 0; i < 8; i++)
        {
            if (cube[i] < surface)
                configurationIndex |= 1 << i;
        }
        return configurationIndex;
    }

    private void BuildMesh() {
        meshFilter.sharedMesh.vertices = vertices.ToArray();
        meshFilter.sharedMesh.triangles = triangles.ToArray();
        meshFilter.sharedMesh.RecalculateNormals();
    }

    private void ClearMeshData() {
        meshFilter.sharedMesh.Clear();
        vertices.Clear();
        triangles.Clear();
    }

    private Vector3Int GetCorner(int cornerIndex) {
        return new Vector3Int(
            (cornerIndex & 4) >> 2,
            (cornerIndex & 2) >> 1,
            cornerIndex & 1
        );
    }

    [ContextMenu("Randomize Voxels")]
    private void Randomize() {
        for (int x = 0; x < size.x; x++) {
            for (int y = 0; y < size.y; y++) {
                for (int z = 0; z < size.z; z++) {
                    voxels[x, y, z].value = Random.Range(0f, 1f);
                }
            }
        }
       
    }

    private void OnDrawGizmos() {
        if (drawBounds) {
            Gizmos.color = Color.white;
            Gizmos.DrawWireCube((Vector3)size / 2, size);
        }

        if (drawVoxels) {
            for (int x = size.x - 1; x >= 0; x--) {
                for (int y = 0; y < size.y; y++) {
                    for (int z = size.z - 1; z >= 0; z--) {
                        if (voxels[x, y, z].value >= surface) {
                            Gizmos.color = Color.Lerp(Color.black, Color.white, voxels[x, y, z].value);
                            Gizmos.DrawCube(new Vector3(x + 0.5f, y + 0.5f, z + 0.5f), Vector3.one * 0.1f);   
                        }
                    }
                }
            }
        }

        if (drawVerticies) {
            Gizmos.color = Color.red;
            foreach (Vector3? vertex in vertexMap)
            {
                if (vertex != null) {
                    Gizmos.DrawSphere((Vector3)vertex + new Vector3(0.5f, 0.5f, 0.5f), 0.1f);
                }
            }
        }
    }

    private void OnValidate() {
        if (size.x != voxels.GetLength(0) || size.y != voxels.GetLength(1) || size.z != voxels.GetLength(2))
        {
            size.Clamp(new Vector3Int(1, 1, 1), new Vector3Int(16, 16, 16));
            voxels = new Voxel[size.x, size.y, size.z];
            for (int x = 0; x < size.x; x++) {
                for (int y = 0; y < size.y; y++) {
                    for (int z = 0; z < size.z; z++) {
                        voxels[x, y, z] = new Voxel();
                    }
                }
            }
            vertexMap = new Vector3?[size.x, size.y, size.z, 3];
            indexMap = new int[size.x, size.y, size.z, 3];
        }

        surface = Mathf.Clamp01(surface);

        if (meshFilter == null) {
            meshFilter = GetComponent<MeshFilter>();
        }
        if (meshFilter.sharedMesh == null) {
            meshFilter.sharedMesh = new Mesh();
        }
    }

